
# variable

nombres_masc=["Alvaro", "Diego", "Leo", "Jacinto", "David"]

# agregar un elemento al final de la lista

nombres_masc.append("Jose")
print(nombres_masc)

# agregar varios elementos al final de la lista

nombres_masc.extend(["Lamine", "Gerardo"])
print(nombres_masc)

# agregar un elemento en una posicion determinada

nombres_masc.insert(0,  "Ricky") # Posicion, nuevo elemento
print(nombres_masc)





































































