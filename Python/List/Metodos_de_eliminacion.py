
# variable

nombres_masc=["Alvaro", "Diego", "Leo", "Jacinto", "David"]

# eliminar el ultimo elemento de la lista

nombres_masc.pop()
print(nombres_masc)


# eliminar un elemento por su indice

nombres_masc.pop(2)
print(nombres_masc)

# elimnar un elemento por su valor

nombres_masc.remove("Alvaro")
print(nombres_masc)








































