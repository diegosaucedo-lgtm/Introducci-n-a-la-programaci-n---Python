
# variable

nombres_masc=["Alvaro", "Diego", "Leo", "Jacinto", "David"]

# ordenar una lista en reversa (invertir orden)

nombres_masc.reverse()
print(nombres_masc)

# ordenar una lista en forma ascendente

nombres_masc.sort()
print(nombres_masc)

# ordenar una lista en forma descendente

nombres_masc.sort(reverse=True)
print(nombres_masc)























