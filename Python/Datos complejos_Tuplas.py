
# TUPLAS: almacena datos diferentes que no pueden ser modificados una vez creados

mi_tupla=("Hola como están", 45, 5.89, 'A')

# Subindices desdes 0 hacia adelante

print(mi_tupla[0])
print("")
print(mi_tupla[0:2]) # Devuelve los datos entre los indices 0 y 1
print("")
print(mi_tupla[:2])
print("")
print(mi_tupla[2:])
print("")

# Indice negativo (desde derechaa a izquierda)

print(mi_tupla[-1])
print("")
print(mi_tupla[-2])
print("")